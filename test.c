#include <stdio.h>

int main() {
    int a = 10;
    int b = 20;

    if (a > b) {
        printf("a es mayor que b\n");
    } else {
        printf("b es mayor o igual a a\n");
    }

    // Ciclo for
    for (int i = 0; i < 10; i++) {
        printf("i: %d\n", i);
    }

    /* Comentario de una línea
       seguido por otra línea */

    return 0;
}